#include<bits/stdc++.h>
using namespace std;
int main()
{

    int n, m;
    cin >> n >> m;

    for(int i = 0; i < n; i++){ //? O(N)
        for(int j = 0; j < m; j++){ //? O(N)
            for(int k = 0; k < n; k++){ //? O(N)
                cout << "Hello World" << endl;
            }
        }
    }
    return 0;
}